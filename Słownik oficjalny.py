#Keyword searching:

def find_keyword(keyword):
    keyword = keyword.lower()
    if keyword in dictionary_WT:
        return f"{dictionary_WT[keyword]}"
    else:
        return None


# Adding new keyword:

def add_keyword(keyword, definition):
    keyword = keyword.lower()
    if keyword not in dictionary_WT:
        dictionary_WT[keyword] = definition
        return f"Definicja '{keyword}' została dodana do słownika."
    else:
        return f"Definicja '{keyword}' już istnieje w słowniku."

# Editing existing keyword:

def edit_keyword(keyword, new_definition):
    keyword = keyword.lower()
    if keyword in dictionary_WT:
        dictionary_WT[keyword] = new_definition
        return f"Definicja '{keyword}' została zaktualizowana."
    else:
        return f"Definicja '{keyword}' nie istnieje w słowniku."

# Request to delete existing keyword:
def delete_keyword(keyword):
    keyword = keyword.lower()
    if keyword in dictionary_WT:
        return f"Definicja '{keyword}' została zgłoszona do usunięcia."
        del dictionary_WT[keyword]
    else:
        return f"Definicja '{keyword}' nie istnieje w słowniku."

