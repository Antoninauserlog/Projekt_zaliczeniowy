        if choice == "1":
            while True:
                keyword = input("Podaj pojęcie: ")
                result = find_keyword(keyword)
                if result:
                    print(f'{result}\n')
                    break
                else:
                    print("Słowo nie zostało znalezione w słowniku. Spróbuj ponownie.")
